export function getMetricValue(city: CityMarket, metric: CityMetricKey): number {
  if (metric === 'demand') return city.demandIndex
  if (metric === 'adr') return city.adrIndex
  if (metric === 'channel') return city.channelHealth
  return city.eventImpact
}
